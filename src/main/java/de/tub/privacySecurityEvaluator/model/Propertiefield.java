package de.tub.privacySecurityEvaluator.model;

public class Propertiefield {
    private String name;
    private String unit;

    public Propertiefield() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }
}
